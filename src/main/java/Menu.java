    public void addTable(String name, int qty, double price) {

        dt = (DefaultTableModel) jTable1.getModel(); //getModel() Method returns information on the current data model during the processing of a personalized reverse engineering.

        DecimalFormat df = new DecimalFormat("00.00");
        String SubTotalPrice = df.format(price * qty);

        // ang ginagawa nito is para hindi madoble yung name na na-select sa menu
        for (int row = 0; row < jTable1.getRowCount(); row++) {
            if (name == jTable1.getValueAt(row, 0)) {
                dt.removeRow(jTable1.convertRowIndexToModel(row));
            }
        }

        Vector v = new Vector();

        v.add(name);
        v.add(qty);
        v.add(SubTotalPrice);

        dt.addRow(v);

    }

    public void calculate() {
        //calculate total table values
        double total = 0;

        for (int i = 0; i < jTable1.getRowCount(); i++) {
            total += Double.parseDouble(jTable1.getValueAt(i, 2).toString());
        }

        DecimalFormat df = new DecimalFormat("00.00");
        jLabelTotal.setText(df.format(total)); //for TOTAL
    }

