            billP.bill.setText(billP.bill.getText() + " Change: " + jLabelChange.getText() + "\n");
            billP.bill.setText(billP.bill.getText() + " ---------------------------------------------------------------------------------- \n");
            billP.bill.setText(billP.bill.getText() + " \tThank You! Come Again!!!\n");
